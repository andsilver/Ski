                  <p>If you like ancient villages then Grimentz has to be seen. Its located in the heart of the Val d'Anniviers at a height of 1,564 to the south of Sion and was once an old farming village. Evidence of the old days can still be sen today with its narrow cobbled streets, water wheel used to grind the wheat and the old raccards (barns) used to store the flour.</p>
